package ecarx.carext.base;

/**
 * Indicates the function status.
 *
 * @since 1
 */
public enum FunctionStatus {
    /**
     * The function is available.
     *
     * @since 1
     */
    AVAILABLE,
    /**
     * The function is unavailable.
     *
     * @since 1
     */
    UNAVAILABLE,
    /**
     * The function has an error.
     *
     * @since 1
     */
    ERROR
}